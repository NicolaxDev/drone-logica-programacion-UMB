while True:
    for row in map:
        for element in row:
            print(element, end=" ")
        print()
    
    print("Presione W, A, S o D para moverse. Q para salir.")
    tecla = input(">> ").strip().lower()

    if tecla == "q":
        print("Saliendo...")
        break

    dy, dx = 0, 0
    if tecla == "w":
        dy = -1
    elif tecla == "s":
        dy = 1
    elif tecla == "a":
        dx = -1
    elif tecla == "d":
        dx = 1
    else:
        print("Movimiento no válido.")
        continue

    ny = y + dy
    nx = x + dx

    if not (0 <= ny < len(map) and 0 <= nx < len(map[0])):
        print("No puedes salirte del mapa.")
        continue

    destino = map[ny][nx]

    if destino == "#":
        print("############################### Colisión con muro, no puedes pasar ###############################")
        continue
    elif destino == "T":
        map[y][x] = " "
        map[ny][nx] = "D"
        for row in map:
            for element in row:
                print(element, end=" ")
            print()
        print("Llegaste al objetivo")
        break
    else:
        map[y][x] = " "
        map[ny][nx] = "D"
        y, x = ny, nx

